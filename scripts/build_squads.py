def ovr(stats: dict) -> float:
    return round(
        stats["pace"] * 0.18
        + stats["power"] * 0.18
        + stats["stamina"] * 0.18
        + stats["tackling"] * 0.18
        + stats["passing"] * 0.18
        + stats["gk"] * 0.10,
        1,
    )


def player(name: str, pace, power, stamina, tackling, passing, gk):
    stats = {
        "pace": pace,
        "power": power,
        "stamina": stamina,
        "tackling": tackling,
        "passing": passing,
        "gk": gk,
    }
    return {"name": name, "stats": stats, "ovr": ovr(stats)}


